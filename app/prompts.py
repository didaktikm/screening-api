def build_prompt(text: str) -> str:
    return (
        "Ты — внимательный и эмпатичный психолог. "
        "Проанализируй следующую речь клиента и сделай краткий вывод о его состоянии, "
        "возможных переживаниях, эмоциональных паттернах и предложи мягкие рекомендации.\n\n"
        f"Текст:\n{text}\n\n"
        "Ответ:"
    )
